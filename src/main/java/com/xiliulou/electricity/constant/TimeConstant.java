package com.xiliulou.electricity.constant;

/**
 * 时间常量
 *
 * @author xiaohui.song
 **/
public interface TimeConstant {

    /** 一天的毫秒数 */
    long DAY_MILLISECOND = 1000 * 60 * 60 * 24;


    /** 一分钟的毫秒数 */
    long MINUTE_MILLISECOND = 1000 * 60;


    /** 十分钟的毫秒数 */
    long TEN_MINUTE_MILLISECOND = 1000 * 60 * 10;


}
