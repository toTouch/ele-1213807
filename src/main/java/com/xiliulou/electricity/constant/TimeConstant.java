package com.xiliulou.electricity.constant;

/**
 * 时间常量
 *
 * @author xiaohui.song
 **/
public interface TimeConstant {
    
    /**
     * 一天的小时
     */
    Integer DAT_HOURS = 24;
    /**
     * 一天的毫秒数
     */
    long DAY_MILLISECOND = 1000 * 60 * 60 * 24;
    
    
    /**
     * 一分钟的毫秒数
     */
    long MINUTE_MILLISECOND = 1000 * 60;
    
    
    /**
     * 十分钟的毫秒数
     */
    long TEN_MINUTE_MILLISECOND = 1000 * 60 * 10;
    
    /**
     * 一小时的毫秒数
     */
    long HOURS_MILLISECOND = 60 * 60 * 1000L;
    
    /**
     * 十秒的毫秒数
     */
    long TEN_SECOND_MILLISECOND = 10 * 1000L;
    
    /**
     * 三秒的毫秒数
     */
    long THREE_SECOND_MILLISECOND = 3 * 1000L;
    
    /**
     * 一小时的分钟数
     */
    long HOURS_MINUTE = 60L;
    
    Integer ONE_MONTH = 30;
    
}
